// 迭代优化 Prompt（多轮专用）
// ============================================================

function getIterationHint(round: number, aiProbability: number): string {
  if (round <= 1) return '';

  const hints: string[] = [];

  hints.push(`\n## ⚠️ 这是第${round}轮迭代优化`);
  hints.push(`上一轮处理后的AI检测概率为 ${aiProbability}%，需要进一步降低。`);

  if (aiProbability > 60) {
    hints.push(`### 高危：AI概率仍然很高（>${60}%），需要大幅改动`);
    hints.push(`- 打破更多句式对称结构`);
    hints.push(`- 增加更多不完美表达：重复、省略、碎碎念`);
    hints.push(`- 每3句话至少有1句是3个字以内的短句`);
    hints.push(`- 大量注入语气词和口语化表达`);
    hints.push(`- 可以加入更多打断和语序变化`);
  } else if (aiProbability > 30) {
    hints.push(`### 中等：AI概率中等（30-60%），需要针对性优化`);
    hints.push(`- 重点攻击连续长句段落，拆成更多短句`);
    hints.push(`- 增加更多"意外表达"——不常规但有生命力的说法`);
    hints.push(`- 加大语气词密度`);
    hints.push(`- 制造更多句子长度波动`);
  } else {
    hints.push(`### 良好：AI概率已经较低（<30%），做微调即可`);
    hints.push(`- 只需在个别过于"完美"的段落注入不完美`);
    hints.push(`- 确保没有连续3句以上句式相似的情况`);
    hints.push(`- 不要过度修改，保持当前的良好状态`);
  }

  return hints.join('\n');
}

// ============================================================
// Prompt 生成
// ============================================================

export function generateDeAIPrompt(options: DeAIPromptOptions): string {
  const {
    originalContent,
    level,
    genre,
    dialectStyle = 'none',
    previousResult,
    previousAiProbability,
    iterationRound = 1,
  } = options;

  if (level === 'none') {
    return originalContent;
  }

  const levelInstruction = levelInstructions[level];
  const dialectTemplate = DIALECT_TEMPLATES[dialectStyle] || DIALECT_TEMPLATES.none;
  const dialectInstruction = dialectTemplate.instruction;
  const genreHint = genre ? genreStyleHints[genre] || '' : '';
  const iterationHint = previousAiProbability !== undefined
    ? getIterationHint(iterationRound, previousAiProbability)
    : '';

  // 如果是迭代轮次，使用上一轮结果作为输入
  const contentToProcess = previousResult || originalContent;

  return `${systemPersona}

${genre ? `当前剧本题材：${genre}${genreHint ? '（' + genreHint + '）' : ''}` : ''}
${dialectInstruction}

${levelInstruction}

${iterationHint}

---

## 原文（需要改写的内容）

${contentToProcess}

---

## 输出规则
1. 直接输出改写后的完整剧本，不要加任何说明、标注、解释
2. 不要说"以下是改写后的版本"之类的话
3. 保留所有集数标记（【第X集】）
4. 保留所有格式标记（零桢画面、▶、OS、VO、[特写]等）
5. 改写后的文本长度应与原文接近（允许±20%浮动）${iterationRound > 1 ? '\n6. 这是迭代优化轮次，请在上一轮基础上进一步降低AI痕迹' : ''}`;
}

// ============================================================
// 批量处理 Prompt
// ============================================================

export function generateBatchDeAIPrompt(
  episodes: { number: number; content: string }[],
  level: AIFlavorLevel,
  genre?: string,
  dialectStyle?: string
): string {
  if (level === 'none') {
    return episodes.map(ep => ep.content).join('\n\n---\n\n');
  }

  const levelInstruction = levelInstructions[level as keyof typeof levelInstructions] || '';
  const dialectTemplate = DIALECT_TEMPLATES[dialectStyle || 'none'] || DIALECT_TEMPLATES.none;
  const dialectInstruction = dialectTemplate.instruction;

  return `${systemPersona}

${genre ? `当前剧本题材：${genre}` : ''}
${dialectInstruction}

${levelInstruction}

---

## 需要改写的剧本

${episodes.map(ep => `【第${ep.number}集】\n${ep.content}`).join('\n\n---\n\n')}

---

## 输出规则
1. 按顺序输出每一集的改写结果，使用【第X集】标记区分
2. 不要输出任何说明或解释
3. 保留所有格式标记
4. 每集改写后的长度与原文接近`;
}

// ============================================================
// 响应解析
// ============================================================

export function parseDeAIResponse(response: string, episodeCount: number): string[] {
  const episodes: string[] = [];
  const parts = response.split(/【第(\d+)集】/);

  for (let i = 1; i < parts.length; i += 2) {
    const content = parts[i + 1] || '';
    episodes.push(content.trim());
  }

  if (episodes.length !== episodeCount) {
    const alternatives = response.split(/---\n?/);
    if (alternatives.length >= episodeCount) {
      return alternatives.slice(0, episodeCount).map(a => a.trim());
    }
  }

  return episodes.length > 0 ? episodes : [response];
}
